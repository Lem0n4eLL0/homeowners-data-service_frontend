export const fetchNews = (
  page: number,
  limit: number
): Promise<{ items: INewsItem[]; hasMore: boolean }> => {
  return new Promise(resolve => {
    setTimeout(() => {
      const start = (page - 1) * limit;
      const end = start + limit;
      const items = newsPool.slice(start, end);

      resolve({
        items,
        hasMore: end < newsPool.length,
      });
    }, 800);
  });
};
